package com.myscript.iink.demo.models;

public class Rect {
    public float left;
    public float top;
    public float right;
    public float bottom;

    public Rect(float v, float v1, float v2, float v3) {
        this.left = v;
        this.top = v1;
        this.right = v2;
        this.bottom = v3;
    }
}
